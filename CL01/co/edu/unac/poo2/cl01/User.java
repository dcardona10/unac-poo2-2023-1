package co.edu.unac.poo2.cl01;

public class User {
    
    public String id;
    public String name;
    public String address;
    public String phoneNumber;

    public User() {
    
    }

    public User(String id, String name, String address, String phoneNumber) {
        this.id = id;
        this.name = name;
        this.address = address;
        this.phoneNumber = phoneNumber;
    }
}
